public class Puerto {
    private String nombre;
    public String ubicacion;
    private int capacidadMaxima;
    public String telefonoContacto;
    private Amarre[] amarres;
    private Embarcacion[] embarcaciones;
    private String[] matriculasAmarradas;

    public Puerto(String nombre, String ubicacion, int capacidadMaxima, Amarre[] amarres, Embarcacion[] embarcaciones, String[] matriculasAmarradas) {
        this.nombre = nombre;
        this.ubicacion = ubicacion;
        this.capacidadMaxima = capacidadMaxima;
        this.amarres = amarres;
        this.embarcaciones = embarcaciones;
        this.matriculasAmarradas = matriculasAmarradas;
    }
























}